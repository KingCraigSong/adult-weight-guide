function matchesCalorieBand(meal, calorieBand) {
  if (!calorieBand || calorieBand === 'all') return true
  if (calorieBand === 'under-300') return meal.calories <= 300
  if (calorieBand === '301-450') return meal.calories >= 301 && meal.calories <= 450
  if (calorieBand === 'over-450') return meal.calories > 450
  return true
}

export function filterWeightLossMeals(items, filters = {}) {
  return items.filter((meal) => {
    const mealMatches = !filters.meal || filters.meal === '全部餐次' || meal.meal === filters.meal
    const methodMatches = !filters.method || filters.method === '全部方式' || meal.method === filters.method
    const categoryMatches = !filters.category || filters.category === '全部类型' || meal.category === filters.category
    const prepMatches = !filters.prepTiming || filters.prepTiming === 'all' || meal.prepTiming === filters.prepTiming
    const portableMatches = !filters.portableOnly || meal.portable
    return mealMatches && methodMatches && categoryMatches && prepMatches && portableMatches && matchesCalorieBand(meal, filters.calorieBand)
  })
}
